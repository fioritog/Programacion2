/*1) BIBLIOTECA "Lectura Viva"
 Una biblioteca local llamada "Lectura Viva" desea implementar un sistema que les permita gestionar los préstamos de libros
a sus miembros. La biblioteca tiene varios libros disponibles, y cada miembro puede tomar prestado un libro a la vez.

Entidades principales del sistema:
Miembro: Es una persona registrada en la biblioteca, que tiene un nombre, un número de identificación único y una fecha de
registro.
Libro: Cada libro tiene un título, un autor y un código ISBN que lo identifica de manera única.
Préstamo: El préstamo representa la acción de un miembro que toma un libro prestado. El préstamo incluye la fecha en que se
prestó el libro y la fecha en que debe ser devuelto.
 */
